public class IntroToArrays {
    public static void main(String[] args) {
        String[] kingdoms = {"Mercia", "Wessex", "Northumbria", "East Anglia"};
        System.out.println();
        for (int i = 0; i < kingdoms.length; i++) {
            System.out.println(kingdoms[i]);
        }
        System.out.println();
    }
}